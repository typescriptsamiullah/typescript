"use strict";
// They think of something you could store in a TypeScript Object. Write a program that creates Objects containing these items.
// TypeScript objects for animals
let lion = {
    name: "Lion",
    habitat: "Grasslands",
    diet: "Carnivore",
    averageWeightKg: 190,
};
let elephant = {
    name: "Elephant",
    habitat: "Savannah",
    diet: "Herbivore",
    averageWeightKg: 6000,
};
let dolphin = {
    name: "Dolphin",
    habitat: "Ocean",
    diet: "Carnivore",
    averageWeightKg: 1500,
};
// Print information about the animals
console.log("Animal Information:");
console.log("Name:", lion.name);
console.log("Habitat:", lion.habitat);
console.log("Diet:", lion.diet);
console.log("Average Weight (kg):", lion.averageWeightKg);
console.log("\nName:", elephant.name);
console.log("Habitat:", elephant.habitat);
console.log("Diet:", elephant.diet);
console.log("Average Weight (kg):", elephant.averageWeightKg);
console.log("\nName:", dolphin.name);
console.log("Habitat:", dolphin.habitat);
console.log("Diet:", dolphin.diet);
console.log("Average Weight (kg):", dolphin.averageWeightKg);
